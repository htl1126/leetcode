# Definition for a  binary tree node
class TreeNode(object):
    def __init__(self, x):
        self.val = x
        self.left = None
        self.right = None


class BSTIterator(object):
    def __init__(self, root):
        """
        :type root: TreeNode
        """
        self.stack = []
        self.pushAll(root)

    def hasNext(self):
        """
        :rtype: bool
        """
        return self.stack

    def next(self):
        """
        :rtype: int
        """
        tmp_node = self.stack.pop()
        self.pushAll(tmp_node.right)
        return tmp_node.val

    def pushAll(self, node):
        while node:
            self.stack.append(node)
            node = node.left

